function isPrime(num) {
	if (num <= 1 || num > 1000) {
	  console.log("Данные неверны");
	  return;
	}
	for (let i = 2; i <= Math.sqrt(num); i++) {
	  if (num % i === 0) {
		 console.log("Составное число");
		 return;
	  }
	}
	console.log("Простое число");
 }
 